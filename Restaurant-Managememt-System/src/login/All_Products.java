    private void Cold_Drinks_PannelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Cold_Drinks_PannelMouseClicked
        // TODO add your handling code here:
        String name_drink[] =  {"CocaCola","Pepsi","Sprite","7Up","Dew","Fanta","Marinda"};
        JComboBox cb1 = new JComboBox(name_drink);
        int Input = JOptionPane.showConfirmDialog(rootPane, cb1,"Select Size",JOptionPane.DEFAULT_OPTION);

        if(Input == JOptionPane.OK_OPTION)
        {
            String name = (String)cb1.getSelectedItem();

            String size_drink[] =  {"Regular","1/2 Liter","1 Liter","1.5 Liter","2.25 Liter"};
            JComboBox cb2 = new JComboBox(size_drink);
            int Size = JOptionPane.showConfirmDialog(rootPane, cb2,"Select Size",JOptionPane.DEFAULT_OPTION);

            if (Size == JOptionPane.OK_OPTION)
            {
                String size = cb2.getSelectedItem().toString();

                if(size == "Regular")
                {
                    int price = 30;
                    All_Products(name,price);
                }
                else if(size == "1/2 Liter")
                {
                    int price = 45;
                    All_Products(name,price);
                }
                else if(size == "1 Liter")
                {
                    int price = 70;
                    All_Products(name,price);
                }
                else if(size == "1.5 Liter")
                {
                    int price = 90;
                    All_Products(name,price);
                }
                else
                {
                    int price = 120;
                    All_Products(name,price);
                }
            }
        }
    }//GEN-LAST:event_Cold_Drinks_PannelMouseClicked

